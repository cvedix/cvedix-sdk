
#pragma once

#include "cvedix_control_meta.h"

namespace cvedix_objects {
    // control meta for video recording, it is a specific type of cvedix_control_meta.
    // when cvedix_record_node handle this control meta, the node will start recording video asynchronously, begin with the Latest Next frame in pipeline (pre-record frames excluded).
    // refer to ./nodes/record/README.md for more details
    class cvedix_video_record_control_meta: public cvedix_control_meta {
    private:
        /* data */
    public:
        cvedix_video_record_control_meta(int channel_index, 
                                    std::string video_file_name_without_ext, 
                                    int record_video_duration = 0,
                                    bool osd = false);
        ~cvedix_video_record_control_meta();

        // copy myself
        virtual std::shared_ptr<cvedix_meta> clone() override;

        // file name without extension, like 2022-10-20_22-30-20_aaa_bbb, which should be a meaningful and unique name.
        // generated by sender who want to record video
        std::string video_file_name_without_ext;

        // record video duration (seconds), not including pre-record time.
        // 0 means using the value setted by cvedix_record_node.
        int record_video_duration = 0;

        // record type (osd frame or not)
        bool osd = false;
    };

}